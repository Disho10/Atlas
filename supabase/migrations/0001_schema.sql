-- ----------------------------------------------------------------------------
-- ORDERS
-- ----------------------------------------------------------------------------
create type order_status as enum ('placed', 'confirmed', 'shipped', 'delivered', 'cancelled');
create type order_channel as enum ('website', 'instagram', 'whatsapp');
create type payment_method as enum ('whish_pay', 'omt', 'card', 'cod');

create sequence order_number_seq start 10001;

create table orders (
  id uuid primary key default uuid_generate_v4(),
  order_number text unique,
  user_id uuid references profiles(id) on delete set null,
  status order_status not null default 'placed',
  channel order_channel not null default 'website',
  payment_method payment_method not null,
  customer_name text not null,
  customer_phone text,
  customer_email text,
  address text not null,
  city text,
  subtotal_usd numeric(10,2) not null,
  is_preorder boolean not null default false,
  logged_by uuid references profiles(id), -- set when admin manually logs an IG/WhatsApp order
  telegram_notified boolean not null default false,
  receipt_sent boolean not null default false,
  created_at timestamptz not null default now()
);

create function generate_order_number()
returns trigger as $$
begin
  new.order_number := 'ATL-' || nextval('order_number_seq')::text;
  return new;
end;
$$ language plpgsql;

create trigger set_order_number
  before insert on orders
  for each row
  when (new.order_number is null)
  execute procedure generate_order_number();

create table order_items (
  id uuid primary key default uuid_generate_v4(),
  order_id uuid not null references orders(id) on delete cascade,
  product_id uuid references products(id),
  product_name text not null, -- snapshot in case product changes later
  size text,
  qty int not null default 1,
  unit_price_usd numeric(10,2) not null
);

create table return_requests (
  id uuid primary key default uuid_generate_v4(),
  order_id uuid not null references orders(id) on delete cascade,
  user_id uuid references profiles(id),
  type text not null check (type in ('return', 'exchange')),
  reason text not null,
  status text not null default 'submitted' check (status in ('submitted', 'approved', 'rejected', 'completed')),
  created_at timestamptz not null default now()
);

-- ----------------------------------------------------------------------------
-- REFERRALS
-- ----------------------------------------------------------------------------
create table referral_redemptions (
  id uuid primary key default uuid_generate_v4(),
  code text not null references profiles(referral_code),
  redeemed_by uuid references profiles(id),
  order_id uuid references orders(id),
  reward_usd numeric(10,2) not null default 10,
  created_at timestamptz not null default now()
);

-- ----------------------------------------------------------------------------
-- SEARCH ANALYTICS
-- ----------------------------------------------------------------------------
create table search_logs (
  id uuid primary key default uuid_generate_v4(),
  term text not null,
  result_count int not null,
  user_id uuid references profiles(id),
  created_at timestamptz not null default now()
);

-- Helpful index for the zero-result-search admin view
create index idx_search_logs_zero_results on search_logs (term) where result_count = 0;
create index idx_products_league on products(league_slug);
create index idx_products_category on products(category);
create index idx_orders_status on orders(status);
